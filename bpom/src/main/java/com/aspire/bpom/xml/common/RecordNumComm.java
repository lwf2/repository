package com.aspire.bpom.xml.common;

/**
 * 签约、订单对账总记录数
 * @author liuweifeng
 *
 */
public class RecordNumComm {

	/** 总记录数 */
	private Integer totalRecord;
	/** 签约用户记录条数 */
	private Integer signRecord;
	/** 解约用户记录条数 */
	private Integer unSignRecord;
	
	public Integer getTotalRecord() {
		return totalRecord;
	}
	public void setTotalRecord(Integer totalRecord) {
		this.totalRecord = totalRecord;
	}
	public Integer getSignRecord() {
		return signRecord;
	}
	public void setSignRecord(Integer signRecord) {
		this.signRecord = signRecord;
	}
	public Integer getUnSignRecord() {
		return unSignRecord;
	}
	public void setUnSignRecord(Integer unSignRecord) {
		this.unSignRecord = unSignRecord;
	}
}
