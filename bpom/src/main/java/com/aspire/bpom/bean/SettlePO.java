package com.aspire.bpom.bean;


public class SettlePO 
{
    /*
     * 计费平台中交易的订单号
     */
    private String orderId;
    /*
     * 任我看传入的交易ID
     */
    private String tradeId;
    /*
     * 用户ID
     */
    private String userId;
    /*
     *产品代码 
     */
    private String productId;
    /*
     * 渠道代码
     */
    private String channelId;
    /*
     * 交易金额  
     * 整数，单位为分
     * 
     */
    private String totalFee;
    /*
     * 有效取值
     * P：付款
     * R：退款
     */
    private String transType;
    /*
     * 交易流水号，支付网关内部交易ID 
     */
    private String requestId;
    /*
     * 提交支付时间
     * 格式为：
     * YYYYMMDDHHMISS
     */
    private String submitTime;
    /*
     * 第三方支付机构代码
     * 参考附录定义
     * 目前取值“WEIXIN”
     */
    private String payOrganization;
    /*
     * 支付通道代码
     * 参考附录定义
     * 目前取值：
     * H5：H5支付
     * WX-JSAPI：微信公众号
     * WX-PAP：微信委托代扣支付 
     */
    private String payWap;
    /*
     * 支付方式代码
     * 参考附录定义
     * 目前填默认方式：
     * 1：基本账户支付
     */
    private String payType;
    /*
     * 交易完成时间
     * 付款或者退款时间，格式为：YYYYMMDDHHMISS
     * 取值为第三方支付平台交易完成时间，从第三方支付平台反馈消息中获取。
     */
    private String payTime;
    
    public String getOrderId()
    {
    
        return orderId;
    }
    
    public void setOrderId(String orderId)
    {
    
        this.orderId = orderId;
    }
    
    public String getTradeId()
    {
    
        return tradeId;
    }
    
    public void setTradeId(String tradeId)
    {
    
        this.tradeId = tradeId;
    }
    
    public String getUserId()
    {
    
        return userId;
    }
    
    public void setUserId(String userId)
    {
    
        this.userId = userId;
    }
    
    public String getProductId()
    {
    
        return productId;
    }
    
    public void setProductId(String productId)
    {
    
        this.productId = productId;
    }
    
    public String getChannelId()
    {
    
        return channelId;
    }
    
    public void setChannelId(String channelId)
    {
    
        this.channelId = channelId;
    }
      
    public String getTotalFee()
    {
    
        return totalFee;
    }

    
    public void setTotalFee(String totalFee)
    {
    
        this.totalFee = totalFee;
    }

    public String getTransType()
    {
    
        return transType;
    }
    
    public void setTransType(String transType)
    {
    
        this.transType = transType;
    }
    
    public String getRequestId()
    {
    
        return requestId;
    }
    
    public void setRequestId(String requestId)
    {
    
        this.requestId = requestId;
    }
    
    public String getSubmitTime()
    {
    
        return submitTime;
    }
    
    public void setSubmitTime(String submitTime)
    {
    
        this.submitTime = submitTime;
    }
    
    public String getPayOrganization()
    {
    
        return payOrganization;
    }
    
    public void setPayOrganization(String payOrganization)
    {
    
        this.payOrganization = payOrganization;
    }
    
    public String getPayWap()
    {
    
        return payWap;
    }
    
    public void setPayWap(String payWap)
    {
    
        this.payWap = payWap;
    }
    
    
    public String getPayType()
    {
    
        return payType;
    }

    
    public void setPayType(String payType)
    {
    
        this.payType = payType;
    }

    public String getPayTime()
    {
    
        return payTime;
    }
    
    public void setPayTime(String payTime)
    {
    
        this.payTime = payTime;
    }

    @Override
    public String toString()
    {

        return "SettlePO [orderId=" + orderId + ", tradeId=" + tradeId
               + ", userId=" + userId + ", productId=" + productId
               + ", channelId=" + channelId + ", totalFee=" + totalFee
               + ", transType=" + transType + ", requestId=" + requestId
               + ", submitTime=" + submitTime + ", payOrganization="
               + payOrganization + ", payWap=" + payWap + ", payType="
               + payType + ", payTime=" + payTime + "]";
    }
    
    
}